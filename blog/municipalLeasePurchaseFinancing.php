              <h2>How Your Municipality Can Benefit From Municipal Lease Purchase Financing</h2>
              <p>
                The ways a municipality finances a project vary greatly.  So do the challenges.  
                From state constitutions and statutes, IRS requirements, input from its citizens 
                and the individual unique nature of the project, financing capital projects 
                can face many challenges.   <br/> <br/>
                Lease Purchase Financing can be an effective option for local government, agencies 
                and districts needing to finance a project with limited financial resources and 
                little time to go through the process of issuing a bond.
              </p>

              <p class="blogHeader">The Challenge of Other Financing Options</p>
              <p>
                Suppose your city wants to finance a municipal facility.  What are its options?  
                Some cities may consider financing the project through a traditional loan from their 
                local bank, however, state constitutions may prohibit local governments from incurring 
                debt to fund a facility without voter approval.   <br/> <br/>
                Issuing a general obligation bond might be a legally valid option, but this requires 
                added costs and steps, including, hiring bond counsel, providing public notices and  
                meetings,  and holding a bond election, just to name a few.   <br/> <br/>
                If the city needs the facility or equipment up and going quickly with minimal costs of 
                issuance, Lease Purchase Financing is an option worth considering.
              </p>

              <p class="blogHeader">How Does Lease Purchase Financing Work?</p>
              <p>
                Where allowable, Lease Purchase Financing operates like installment purchase financing and, 
                if structured properly, might not be considered debt for state law purposes.  Voter approval 
                is not required to authorize the transaction.  The city (lessee) pays annual rental payment 
                installments to the financing entity (lessor) to finance the facility&#39;s construction 
                or purchase.    <br/> <br/>
                The lessee agrees to consider budgeting and appropriating the annual rental payment each 
                fiscal year. The annual lease rental payment is subject to annual appropriation and consists 
                of principal and agreed upon interest components, which means that unlike a traditional lease, 
                Lease Purchase Financing builds equity in the project with each payment.    <br/> <br/>
                During the life of the Lease Purchase, the lessee maintains ownership in the facility, with a 
                security interest granted to the lessor.  If the lessee does not appropriate funds and make the 
                rental payment in a given year, based on its agreement, the lease would require the lessee to 
                turn possession of the facility over to the lessor, losing any equity in the project.   <br/> <br/>
                Once the lessee pays the final rental payment installment, the lien held by the lessor is 
                released and the lessee owns the financed facility free and clear of any liens or restrictions.
                <img src="img/how_financing works.jpg" alt="How Does Lease Purchase Financing Work" 
                  width="750" style="width: 100%;"/>
              </p>

              <p class="blogHeader">What Are the Benefits and Downsides?</p>
              <p>
                There are several unique attributes of a Lease Purchase Financing mentioned above that are 
                worth re-emphasizing:   <br/> <br/>
                <b>No voter approval required</b> <br/>
                Voter approval is not required to authorize Lease Purchase Financing allowing for essential 
                equipment and projects to be financed quickly.    <br/> <br/>

                <b>Not subject to a city&#39;s debt limitations</b> <br/>
                Since a Lease Purchase Financing might not be classified as debt for state law purposes, 
                requirements associated with debt, including debt limitations, might avoid being a potential 
                financing constraint.   <br/> <br/>

                <b>Financing is matched to the useful life of the asset</b> <br/>
                The example didn&#39;t illustrate this, but keep in mind that the terms of a Lease Purchase 
                Financing can span the useful life of the asset whether it is technological equipment, a 
                fleet of school buses, or a new building.   <br/> <br/>

                <b>Less cumbersome approval process</b> <br/>
                Utilizing Lease Purchase Financing to finance this proposed city project could provide the 
                city a simplified financing option with an easier approval process and less documentation 
                compared to a bond financing. No bond rating or bond insurance is required, and ongoing 
                reporting is minimal compared to a publicly issued bond.    <br/> <br/>

                <b>Lease Purchase Financing can take advantage of lower tax-exempt rates</b>  <br/> 
                Most municipal projects and equipment can qualify for lower tax-exempt rates. For projects 
                that cannot qualify for tax-exemption, a taxable option is available.   <br/> <br/>

                Lease Purchase Financings have their own shortcomings.  Two of the biggest concerns to keep 
                in mind are, first, since the lease financing is subject to annual appropriation, if the funds 
                are not appropriated then the facility can be lost.  Second, unlike general obligation bonds, 
                Lease Purchase Financings do not create a new revenue stream with which to make the payment.  
                The city must have an existing revenue stream that is sufficient to service the lease.  
                Third, interest rates on bonds, especially general obligation bonds, are often lower than 
                those on Lease Purchase Financings. 
              </p>

